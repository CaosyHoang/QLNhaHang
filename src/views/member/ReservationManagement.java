    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (reservationDetail.getReservationId()!= null) {
            if (reservationService.Delete(reservationDetail.getReservationId())) {
                JOptionPane.showConfirmDialog(null, "Xóa thành công", "Xóa đặt bàn", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE);
                formWindowOpened(null);
                return;
            }
        }
        JOptionPane.showConfirmDialog(null, "Xóa thất bại", "Xóa đặt bàn", JOptionPane.DEFAULT_OPTION, JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void tbReservationMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbReservationMouseClicked
        // TODO add your handling code here:
        DefaultTableModel tableModel = (DefaultTableModel) tbReservation.getModel();
        Vector<Object> courseVector = (Vector<Object>) tableModel.getDataVector().elementAt(tbReservation.getSelectedRow());

        reservationDetail.setReservationId(Integer.valueOf(courseVector.get(1).toString()));
        reservationDetail.setNumber(Integer.valueOf(courseVector.get(2).toString()));
        reservationDetail.setReservationTime(Date.valueOf(courseVector.get(3).toString()));
        reservationDetail.setStatus(courseVector.get(4).toString());
        reservationDetail.setSeats(Integer.valueOf(courseVector.get(5).toString()));
        reservationDetail.setFlag(Integer.valueOf(courseVector.get(6).toString()));
    }//GEN-LAST:event_tbReservationMouseClicked

