    private void tbOrderMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbOrderMouseClicked
        // TODO add your handling code here:
        DefaultTableModel tableModel = (DefaultTableModel) tbOrder.getModel();
        Vector<Object> OrderVector = (Vector<Object>) tableModel.getDataVector().elementAt(tbOrder.getSelectedRow());

        order.setOrderId(Integer.valueOf(OrderVector.get(1).toString()));
        order.setOrderTime(Date.valueOf(OrderVector.get(2).toString()));
        order.setStatus(OrderVector.get(3).toString());
        order.setAccountId(Integer.valueOf(OrderVector.get(4).toString()));
        order.setPrice(Float.parseFloat(OrderVector.get(5).toString()));

        List<OrderDetail> orderDetails = orderService.getAllOrderDetailOrderId(Integer.valueOf(OrderVector.get(1).toString()));

        Account account = accountService.getAccountId(order.getAccountId());
        txtAccountId.setText(account.getAccountId().toString());
        txtFullName.setText(account.getFullName());
        txtPhone.setText(account.getPhone());

        List<MenuItem> menuItems = new LinkedList<>();
        for (int i = 0; i < orderDetails.size(); i++) {
            MenuItem menuItem = menuItemService.getMenuItemId(orderDetails.get(i).getItemId());
            menuItems.add(menuItem);
        }
        DefaultTableModel model = (DefaultTableModel) tbMenuItem.getModel();
        model.setRowCount(0);
        for (int i = 0; i < menuItems.size(); i++) {
            MenuItem item = menuItems.get(i);
            model.addRow(new Object[]{i + 1, item.getItemId(), item.getName(), item.getCategory(), item.getPrice(),
                item.getDescription(), (item.getAvailable() == 1 ? "Còn món" : "Hết món")});
        }
    }//GEN-LAST:event_tbOrderMouseClicked

