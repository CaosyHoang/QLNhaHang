    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        try {
            tbOrder.removeColumn(tbOrder.getColumnModel().getColumn(4));
            tbMenuItem.removeColumn(tbMenuItem.getColumnModel().getColumn(5));
        } catch (Exception e) {
        }

        SimpleDateFormat simpleDateFormat = new SimpleDateFormat("yyyy-MM-dd");
        List<Order> orders = orderService.getAllOrderByAccountId(UserLogin.getAccountId());
        DefaultTableModel model = (DefaultTableModel) tbOrder.getModel();
        for (int i = 0; i < orders.size(); i++) {
            Order orderA = orders.get(i);
            model.addRow(new Object[]{i + 1, orderA.getOrderId(), simpleDateFormat.format(orderA.getOrderTime()), orderA.getStatus(), orderA.getAccountId(), orderA.getPrice()});
        }
    }//GEN-LAST:event_formWindowOpened

