    private void btnApproveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApproveActionPerformed
        // TODO add your handling code here:
        if (reservationDetail.getReservationId() != null) {
            reservationDetail.setStatus("Đặt thành công");
            reservationDetail.setFlag(1);
            reservationService.Update(reservationDetail);
            if (!reservationService.Update(reservationDetail)) {
                JOptionPane.showConfirmDialog(null, "Duyệt thất bại", "Duyệt đặt bàn", JOptionPane.DEFAULT_OPTION, JOptionPane.ERROR_MESSAGE);
                return;
            }

            JOptionPane.showConfirmDialog(null, "Duyệt thành công", "Duyệt đặt bàn", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE);
            formWindowOpened(null);
        }
    }//GEN-LAST:event_btnApproveActionPerformed

