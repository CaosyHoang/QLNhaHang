    private void tbReservationMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbReservationMouseClicked
        // TODO add your handling code here:
        DefaultTableModel tableModel = (DefaultTableModel) tbReservation.getModel();
        Vector<Object> courseVector = (Vector<Object>) tableModel.getDataVector().elementAt(tbReservation.getSelectedRow());
        SimpleDateFormat simpleDateFormat = new SimpleDateFormat("yyyy-MM-dd");
        reservationDetail.setReservationId(Integer.valueOf(courseVector.get(1).toString()));
        reservationDetail.setFullName(courseVector.get(2).toString()); 
        reservationDetail.setNumber(Integer.valueOf(courseVector.get(3).toString()));
        reservationDetail.setReservationTime(java.sql.Date.valueOf(courseVector.get(4).toString()));
        reservationDetail.setStatus(courseVector.get(5).toString());
        reservationDetail.setPhone(courseVector.get(6).toString());
        reservationDetail.setSeats(Integer.valueOf(courseVector.get(7).toString()));
        reservationDetail.setFlag(Integer.valueOf(courseVector.get(8).toString()));
    }//GEN-LAST:event_tbReservationMouseClicked

    private void btnRejectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRejectActionPerformed
        // TODO add your handling code here:

        if (reservationDetail.getReservationId() != null) {
            reservationDetail.setStatus("Đã bị từ chối");
            reservationDetail.setFlag(-1);
            reservationService.Update(reservationDetail);
            int responseConfirm = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn hủy không?", "Hủy đặt bàn", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (responseConfirm == JOptionPane.YES_OPTION) {
                if (!reservationService.Update(reservationDetail)) {
                    JOptionPane.showConfirmDialog(null, "Hủy thất bại", "Hủy đặt bàn", JOptionPane.DEFAULT_OPTION, JOptionPane.ERROR_MESSAGE);
                    return;
                }

                JOptionPane.showConfirmDialog(null, "Hủy thành công", "Hủy đặt bàn", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE);
                formWindowOpened(null);
            }
        }
    }//GEN-LAST:event_btnRejectActionPerformed

    private void btnCompleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCompleteActionPerformed
        // TODO add your handling code here:
        if (reservationDetail.getReservationId() != null) {
            if (!reservationService.Delete(reservationDetail.getReservationId())) {
                JOptionPane.showConfirmDialog(null, "Xóa thất bại", "Xóa đặt bàn", JOptionPane.DEFAULT_OPTION, JOptionPane.ERROR_MESSAGE);
                return;
            }
            JOptionPane.showConfirmDialog(null, "Xóa thành công", "Xóa đặt bàn", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE);
            formWindowOpened(null);
        }
    }//GEN-LAST:event_btnCompleteActionPerformed

