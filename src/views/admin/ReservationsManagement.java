    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        try {
            tbReservation.removeColumn(tbReservation.getColumnModel().getColumn(8));
        } catch (Exception e) {
        }

        SimpleDateFormat simpleDateFormat = new SimpleDateFormat("yyyy-MM-dd");

        List<Reservation> reservations = reservationService.getAllReservations();

        DefaultTableModel model = (DefaultTableModel) tbReservation.getModel();
        model.setRowCount(0);

        for (int i = 0; i < reservations.size(); i++) {
            Reservation reservation = reservations.get(i);
            Table table = tableService.getTableId(reservation.getTableId());
            Account account = accountService.getAccountByTableId(table.getTableId());
            model.addRow(new Object[]{i + 1, reservation.getReservationId(), account.getFullName(), table.getNumber(), simpleDateFormat.format(reservation.getReservationTime()), reservation.getStatus(), account.getPhone(), table.getSeats(), reservation.getFlag()});
        }
    }//GEN-LAST:event_formWindowOpened

